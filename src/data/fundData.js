export const RISK_TOLERANCE = {
  LOW: 'conservative',
  MEDIUM: 'moderate',
  HIGH: 'aggressive'
};

export const mapRiskToleranceToProfile = (riskTolerance) => {
  if (!riskTolerance) return RISK_TOLERANCE.MEDIUM;
  
  const normalizedRisk = riskTolerance.toLowerCase().trim();
  
  switch (normalizedRisk) {
    case 'low':
      return RISK_TOLERANCE.LOW;
    case 'high':
      return RISK_TOLERANCE.HIGH;
    default:
      return RISK_TOLERANCE.MEDIUM;
  }
};

export const getFundsByRisk = (riskType) => {
  if (!riskType) return [];
  
  const normalizedRiskType = riskType.toLowerCase().trim();
  const funds = [];
  const fundCodes = riskProfiles[normalizedRiskType] || [];
  
  // Helper to search through fund categories
  const searchFunds = (fundGroup) => {
    Object.values(fundGroup).forEach(category => {
      if (Array.isArray(category)) {
        category.forEach(fund => {
          if (fundCodes.includes(fund.code)) {
            funds.push(fund)
          }
        })
      } else {
        searchFunds(category)
      }
    })
  }

  searchFunds(mutualFunds)
  return funds
}
